def remove_option(lines, m, option):
    ''' removes option from line m. returns whether we did anything '''
    l = lines[m].find(option)
    if l == -1:
        return False
    val = lines[m][l:].split('"')[1]
    lines[m] = lines[m][:l - 1] + lines[m][l+len(option + '="' + val + '"'):]
    return True


